package com.groceriesapp.model.nutrition;

/**
 * Enum representing common allergen types.
 * Based on FDA's "Big 9" allergens plus other common allergens.
 */
public enum AllergenType {
    // FDA Big 9 Allergens
    MILK,
    EGGS,
    FISH,
    SHELLFISH,
    TREE_NUTS,
    PEANUTS,
    WHEAT,
    SOYBEANS,
    SESAME,
    
    // Other Common Allergens
    GLUTEN,
    CORN,
    SULFITES,
    MUSTARD,
    CELERY,
    LUPIN,
    MOLLUSCS
}
